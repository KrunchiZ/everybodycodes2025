class Node:
    def __init__(self, number):
        self.spine = number
        self.left = ''
        self.right = ''
        self.next = None

class LinkedList:
    def __init__(self):
        self.head = None
    
    def append(self, number):
        new_node = Node(number)
        if not self.head:
            self.head = new_node
            return
        
        current = self.head
        while current.next:
            current = current.next
        current.next = new_node
    
    def prepend(self, data):
        new_node = Node(data)
        new_node.next = self.head
        self.head = new_node
    
    def display(self):
        current = self.head
        while current:
            print(current.spine, end='')
            current = current.next
        print()
    
    def delete(self, number):
        if not self.head:
            return
        
        if self.head.data == data:
            self.head = self.head.next
            return
        
        current = self.head
        while current.next:
            if current.next.data == data:
                current.next = current.next.next
                return
            current = current.next

    def stich_spine(self):
        current = self.head
        number = 0
        while(current):
            number = int(str(number) + str(current.spine))
            current = current.next
        return number

def generate_fishbone(list):
    fishbone = LinkedList()
    for i in range(len(list)):
        if not fishbone.head:
            fishbone.append(list[i])
        else:
            current = fishbone.head
            while current:
                if list[i] < current.spine:
                    if not current.left:
                        current.left = list[i]
                        break
                    else:
                        current = current.next
                elif list[i] > current.spine:
                    if not current.right:
                        current.right = list[i]
                        break
                    else:
                        current = current.next
                else:
                    current = current.next
                if not current:
                    fishbone.append(list[i])
    return fishbone

def compare_bones(listA, listB):
    currentA = listA.head
    currentB = listB.head
    while (currentA and currentB):
        valueA = currentA.spine
        if (currentA.left):
            valueA = int(str(currentA.left) + str(valueA))
        if (currentA.right):
            valueA = int(str(valueA) + str(currentA.right))
        valueB = currentB.spine
        if (currentB.left):
            valueB = int(str(currentB.left) + str(valueB))
        if (currentB.right):
            valueB = int(str(valueB) + str(currentB.right))
        if (valueA < valueB):
            return -1
        elif (valueA > valueB):
            return 1
        else:
            currentA = currentA.next
            currentB = currentB.next
    return 0

